print("Programa para convertir 째C a 째F")
# definir variables
gradosC = 0
gradosF = 0
# input(mensaje) recibe cadenas de texto
# eval(cadena) convierte los textos a numeros
gradosC = eval(input("Ingrese temperatura 째C: "))
# aplicamos la formula
gradosF = (9/5) * gradosC + 32
# output
print("La temperatura en 째F es: ", gradosF)

