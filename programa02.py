print("Programa para convertir de ºC a ºF")
print("==================================")
print("Ingrese la temperatura en grados ºC: ")
# pido la temperatura en ºC input
gradosC = int(input())
# aplico el proceso de formula
gradosF = (9/5) * gradosC + 32.0
# imprimo en ºF
print ("La temperatura en grados ºF", gradosF)
